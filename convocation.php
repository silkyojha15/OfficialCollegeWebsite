            <p>The 2nd Annual Convocation Day was truly a momentous occasion on an unprecedented scale for Government Girls Polytechnic, Ayodhya. The celebrations was held on Thursday, 4th October 2018.The venue was its very own campus ground.</p>
                
            <p>The Chief guest for the Convocation was . He planted a sapling to mark the begining of new chapters in the students life. </p>
            
            <p>The convocation day began with a majestic and grand academic procession followed by welcome dance and prayer song, grand academic procession followed by the National Song sung by the students of the Institute which was notably impressed by the splendid of our ceremonial traditions. The formal function began with a  welcome speech.</p>

            <p>The college day celebrations was kick started  by welcome dance followed by  variety of dance performances and the event of the day was project presentation by final year and second year students.</p>
                
            <p>A scintillating medals and awards ceremony was also part of the Convocation. The medals were also awarded to the winners of Project Presentation and Rangoli Making Competiotion. The graduates were awarded their certificates.</p>
                
            <p>The Chief-Guest made the convocation address. In his speech he said “India represents nearly 30% of the global annual supply of graduate engineers".  Addressing the students he said “ Believe in yourself. Believe in all that you are, all that you can be and all that you stand for. ” He concluded his speech by stressing on the fact    “ Graduation is only a concept. In real life each and every day you graduate. Graduation is a process that goes on until the last day of your life".</p>
                
            <p>The Principal, Shri Vijay Prakash gave the presidential address. He said “This is the biggest milestone to date in the lives of the students, but they have many like this to be crossed". The college is proud that the students are placed in the reputed companies and they are the brand ambassadors of the college. He further said “Now onwards the responsibilities on your shoulder is doubled, as on one hand you have to excel in your career and on the other you have duties towards your family members accomplished”. He advised the students to pursue their higher studies and asked them to  Learn while earning. He further said that, Students must work hard to develop their skills and should consistently work with sincerity, which is the key to success.</p>  
                
            <p>The Principal and HODs distributed degrees to 100 students who had passed out  Diploma, degrees in 2018.</p>
                
            <p>The Convocation ended with a Vote of Thanks by the Principal of college. Guests and students were treated to an evening of excellent food.</p><br>
                
