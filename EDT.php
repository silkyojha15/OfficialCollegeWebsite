                <p>Young students with a vision and ambition to succeed and driven by their dreams to be a successful entrepreneur thronged the venue on the first day of the Entrepreneurship Development Program organized by Government Girls Polytechnic, Ayodhya from the 24th to 26th of October 2018</p>
                
                <p>The speaker spoke about the requisites of a successful entrepreneur and how one could develop skills to become a good entrepreneur. Using phrases like “Success is all about respecting and celebrating failure” he said that in order to be a successful businessman one needs to see failures. Emphasizing on the need to respect time he said that it was important for the students to start putting a price on their time and start developing a death bed mentality. At this age it is of utmost importance that pupils start dedicating their time in nurturing and developing skills which would come in handy at the time when they start business of their own.</p>
                
                <p>The session was an interactive one where students were very participative and engaged in questions with the dignitary. They clarified their views on business and about entrepreneurship. </p>
                
                <p>The last day of the programme was factory visits. The speaker guided the students about each factory and how they work locally and produce finished products.
                The visit to a local factory was also conducted. The students were thrilled to know that how the working of factories takes place. The factory workers also participated enthusiatically by telling the students each working process at every stage. At the end of the visit the students and teachers were gifted few products of the factory as a thank you gift.</p>
                
                <p>The seminar was completely student oriented and had several interactive sessions, where the speaker had some fascinating questions for the students; they actively participated contributing to the innovative ideas and answers. Students were thrilled to discover some unknown facts about “competency and skill”.</p>
                
                <p>On the whole the workshop paved a path to be more informative, interactive and gave students full-fledged insights to become successful entrepreneurs.</p>
                
