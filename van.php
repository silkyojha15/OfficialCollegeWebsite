            <p>The ‘Van Mahotsav’ was celebrated in Government Girls Polytechnic, Ayodhya with a lot of enthusiasm on 14th August ’18. In an age where the jungles are more asphalt than natural, the staff and students came together to plant saplings all along the roads of the campus after breakfast. The sky was completely overcast and there was a light drizzle. One could not have asked for a better weather to plant trees in the rainy season.</p>


            <p>The event started shortly after ten o’clock in the morning where the students with the staff members came out to plant saplings with the gardeners. This was followed with the Pricipal and HODS of college. The students displayed great enthusiasm in wanting to learn how saplings are planted and were constantly asking intelligent questions to the gardeners about the plants.</p>


            <p>The Principal also planted trees in the drive to beautify the campus and bring greenery all around. Nothing is more rewarding than to watch a sapling grow into a tree over the years and then return to the same spot and marvel at the beautiful work that one’s hands have done.</p>


            <p>It was heartening to see the senior functionaries of the school along with the staff, the students and the gardeners work hand-in-hand together as a team. There was frolic and laughter and cheerful faces as everyone was hard at work. The trees planted today will serve as a beautiful memory left behind by the students. For when they come back, they will have indeed a story to tell of how the little sapling they planted grew with them just as they have.</p>


